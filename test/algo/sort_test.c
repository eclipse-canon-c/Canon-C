/*
 * Fuzz algo_sort on int arrays. Invariants:
 *   1. is_sorted after sort (merge path, with temp)
 *   2. is_sorted after sort (insertion path, NULL temp)
 *   3. Sort twice is idempotent (sort(sort(x)) == sort(x))
 *   4. Merge sort and insertion sort produce the same result
 *   5. Sum of elements is unchanged (permutation — nothing added/lost)
 */

#define SORT_FUZZ_MAX 256

static int fuzz_cmp_int(const void* a, const void* b, void* ctx)
{
    (void)ctx;
    int va = *(const int*)a;
    int vb = *(const int*)b;
    return (va > vb) - (va < vb);
}

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)sort_fuzz_suppress_unused;
    (void)sort_slice_suppress_unused;

    if (size < sizeof(int)) return 0;

    usize n = size / sizeof(int);
    if (n > SORT_FUZZ_MAX) n = SORT_FUZZ_MAX;

    int orig[SORT_FUZZ_MAX];
    int arr_merge[SORT_FUZZ_MAX];
    int arr_insert[SORT_FUZZ_MAX];
    int arr_twice[SORT_FUZZ_MAX];
    int tmp[SORT_FUZZ_MAX];

    memcpy(orig,       data, n * sizeof(int));
    memcpy(arr_merge,  data, n * sizeof(int));
    memcpy(arr_insert, data, n * sizeof(int));
    memcpy(arr_twice,  data, n * sizeof(int));

    /* Compute sum before */
    long long sum_before = 0;
    for (usize i = 0; i < n; i++) sum_before += orig[i];

    /* Invariant 1: sort with temp (merge path) produces sorted output */
    algo_sort(arr_merge, n, sizeof(int), fuzz_cmp_int, NULL, tmp);
    if (!algo_is_sorted(arr_merge, n, sizeof(int), fuzz_cmp_int, NULL))
        __builtin_trap();

    /* Invariant 2: sort without temp (insertion path) produces sorted output */
    algo_sort(arr_insert, n, sizeof(int), fuzz_cmp_int, NULL, NULL);
    if (!algo_is_sorted(arr_insert, n, sizeof(int), fuzz_cmp_int, NULL))
        __builtin_trap();

    /* Invariant 4: both paths produce the same result */
    for (usize i = 0; i < n; i++) {
        if (arr_merge[i] != arr_insert[i]) __builtin_trap();
    }

    /* Invariant 3: sort twice is idempotent */
    algo_sort(arr_twice, n, sizeof(int), fuzz_cmp_int, NULL, tmp);
    algo_sort(arr_twice, n, sizeof(int), fuzz_cmp_int, NULL, tmp);
    for (usize i = 0; i < n; i++) {
        if (arr_twice[i] != arr_merge[i]) __builtin_trap();
    }

    /* Invariant 5: sum preserved */
    long long sum_after = 0;
    for (usize i = 0; i < n; i++) sum_after += arr_merge[i];
    if (sum_before != sum_after) __builtin_trap();

    return 0;
}

