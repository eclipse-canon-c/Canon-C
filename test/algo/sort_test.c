/**
 * @file sort_test.c
 * @brief Tests for algo/sort — stable hybrid insertion/merge sort
 *
 * Two functions, three API levels each:
 *   algo_sort        / ALGO_SORT_TYPED        / algo_sort_slice_##type
 *   algo_is_sorted   / ALGO_IS_SORTED_TYPED   / algo_is_sorted_slice_##type
 *
 * Two instantiations:
 *   int   — primary; all three levels exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - Empty (len=0)            — sort: no-op; is_sorted: true
 *   - Single element (len=1)   — sort: no-op; is_sorted: true
 *   - Two elements             — exercises simple swap case
 *   - Small (len < 16)         — insertion sort path
 *   - Large (len >= 16)        — merge sort path (with temp)
 *   - Large, no temp           — insertion sort fallback (NULL temp)
 *   - Already sorted           — is_sorted true; sort is idempotent
 *   - Reverse sorted           — worst case for insertion sort
 *   - All same values          — stable; is_sorted true
 *   - Stability                — equal elements preserve original relative order
 *   - Double sort idempotent   — sort(sort(x)) == sort(x)
 *   - Permutation preservation — same elements before and after sort
 *   - algo_is_sorted false     — first/last/middle pair out of order
 *   - Typed macro              — ALGO_SORT_TYPED / ALGO_IS_SORTED_TYPED
 *   - Typed slice              — algo_sort_slice_int / algo_is_sorted_slice_int
 *   - Point struct             — algo_sort_slice_Point / algo_is_sorted_slice_Point
 *   - Context parameter        — comparator uses ctx for custom ordering
 *
 * Fuzz entry point (CANON_FUZZING):
 *   Input bytes as int[]. Tests:
 *     1. is_sorted after sort (with and without temp)
 *     2. Sort twice = sort once (idempotent)
 *     3. Sum preserved (permutation, no elements lost/added)
 *     4. Merge sort and insertion sort produce the same result
 */

