/*
 * Fuzz algo_filter on a raw byte array.
 *
 * Predicate: keep bytes where value > 127 (high-byte filter).
 *
 * Invariants verified after every call:
 *   1. return value <= out_cap
 *   2. return value == reference count (buffer is always large enough)
 *   3. every element in out satisfies pred
 *   4. order of out matches order of matching elements in input
 */

static bool pred_high_byte(const void* elem, void* ctx)
{
    (void)ctx;
    return *(const u8*)elem > 127;
}

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)filter_fuzz_suppress_unused;
    (void)filter_slice_suppress_unused;

    if (size == 0) return 0;

    /* Output buffer — always large enough to hold all matching elements */
    u8 out[256];
    usize out_cap = sizeof(out);

    usize n = algo_filter(data, size, sizeof(u8), pred_high_byte, NULL,
                          out, out_cap);

    /* Invariant 1: return value <= out_cap */
    if (n > out_cap) __builtin_trap();

    /* Build reference independently */
    u8 ref[256];
    usize ref_count = 0;
    for (usize i = 0; i < size; i++) {
        if (data[i] > 127) ref[ref_count++] = data[i];
    }

    /* Invariant 2: count matches reference */
    if (n != ref_count) __builtin_trap();

    /* Invariant 3: all output elements satisfy pred */
    for (usize i = 0; i < n; i++) {
        if (out[i] <= 127) __builtin_trap();
    }

    /* Invariant 4: output order matches reference order */
    for (usize i = 0; i < n; i++) {
        if (out[i] != ref[i]) __builtin_trap();
    }

    return 0;
}

