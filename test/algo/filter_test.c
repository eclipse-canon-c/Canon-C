/**
 * @file filter_test.c
 * @brief Tests for algo/filter — selective copy matching a predicate
 *
 * Three API levels tested:
 *   Level 1 — Generic (algo_filter): void* interface, any element type
 *   Level 2 — Typed macro (ALGO_FILTER_TYPED): compile-time sizeof
 *   Level 3 — Typed slice (algo_filter_slice_int / algo_filter_slice_Point)
 *
 * Two instantiations:
 *   int   — primary; all three levels exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - algo_filter()             — basic copy of matching elements
 *   - ALGO_FILTER_TYPED()       — same semantics, automatic sizeof
 *   - algo_filter_slice_int()   — typed slice variant
 *   - algo_filter_slice_Point() — struct coverage
 *   - Empty input (len=0)       — returns 0, output untouched
 *   - out_cap == 0              — returns 0, no writes
 *   - All match                 — all elements copied, count == len
 *   - None match                — returns 0, output untouched
 *   - Mixed                     — partial match, order preserved
 *   - Truncation                — return == out_cap, more matches exist
 *   - Exact fit                 — return == out_cap, no truncation
 *   - Context parameter         — pred uses ctx to parameterize threshold
 *   - Stability                 — relative order of matching elements preserved
 *   - Pred call count           — stops calling pred after out_cap matches
 *   - Single element            — matching and non-matching
 *
 * Fuzz entry point (CANON_FUZZING):
 *   - Filters a u8 array for elements above a fixed threshold
 *   - Reference model: independent linear scan of full input
 *   - Invariants checked after every call:
 *       • return value <= out_cap
 *       • return value == reference count (buffer is always large enough)
 *       • every output element satisfies pred
 *       • output order matches order of matching elements in input
 */

