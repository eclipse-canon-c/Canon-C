/**
 * @file unique_test.c
 * @brief Tests for algo/unique — consecutive duplicate removal
 *
 * One function, three API levels:
 *   algo_unique          — generic (void* interface)
 *   ALGO_UNIQUE_TYPED    — typed macro, compile-time sizeof
 *   algo_unique_slice_##type — typed slice
 *
 * Two instantiations:
 *   int   — primary; all three levels exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - Empty (len=0)                    — returns 0, no-op
 *   - Single element                   — returns 1, no-op
 *   - All same values                  — returns 1, single element kept
 *   - No duplicates                    — returns len, nothing moved
 *   - All consecutive duplicates       — pairs reduced to one each
 *   - Run of more than 2 equal values  — entire run collapsed to one
 *   - Mixed runs and unique elements   — correct interleaving
 *   - First and last are equal pairs   — boundary cases
 *   - Two elements equal               — returns 1
 *   - Two elements different           — returns 2
 *   - Consecutive-only (not global)    — non-adjacent duplicates survive
 *   - Sort-then-unique = full dedup    — canonical pattern verified
 *   - Return value accuracy            — n is correct for all cases
 *   - Stale data beyond n              — not read (verified by count)
 *   - Typed macro                      — ALGO_UNIQUE_TYPED
 *   - Typed slice                      — algo_unique_slice_int
 *   - Point struct                     — algo_unique_slice_Point
 *   - Context parameter                — comparator uses ctx
 *   - Idempotency                      — unique(unique(x)) == unique(x)
 *
 * Fuzz entry point (CANON_FUZZING):
 *   Input bytes as int[]. Invariants:
 *     1. Return value n is in [1, len] for non-empty input
 *     2. arr[0..n-1] has no consecutive equal pairs after unique
 *     3. Idempotent: applying unique a second time returns same n
 *     4. n <= len (unique never grows the array)
 *     5. First element is always preserved: arr[0] unchanged
 *     6. Sort-then-unique: n from unique(sort(x)) equals number of
 *        distinct values in x (verified by re-counting)
 */

