/*
 * Fuzz algo_unique on raw int input.
 *
 * Invariants:
 *   1. n in [1, len] for non-empty input (0 for empty)
 *   2. No consecutive equal pairs in arr[0..n-1]
 *   3. Idempotent: unique(unique(x)) returns same n
 *   4. n <= len (never grows)
 *   5. First element preserved: arr[0] unchanged after unique
 *   6. Sort-then-unique: n equals the count of distinct values
 */

static int fuzz_cmp_int(const void* a, const void* b, void* ctx)
{
    (void)ctx;
    int va = *(const int*)a;
    int vb = *(const int*)b;
    return (va > vb) - (va < vb);
}

/* Count distinct values in a sorted array */
static usize count_distinct(const int* arr, usize n)
{
    if (n == 0) return 0;
    usize count = 1;
    for (usize i = 1; i < n; i++) {
        if (arr[i] != arr[i-1]) count++;
    }
    return count;
}

#define UNIQUE_FUZZ_MAX 256

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)unique_fuzz_suppress_unused;
    (void)unique_slice_suppress_unused;

    if (size < sizeof(int)) return 0;

    usize n = size / sizeof(int);
    if (n > UNIQUE_FUZZ_MAX) n = UNIQUE_FUZZ_MAX;

    int arr[UNIQUE_FUZZ_MAX];
    int arr2[UNIQUE_FUZZ_MAX];
    int tmp[UNIQUE_FUZZ_MAX];
    memcpy(arr,  data, n * sizeof(int));
    memcpy(arr2, data, n * sizeof(int));

    int first = (n > 0) ? arr[0] : 0;

    /* Invariants 1, 2, 4 */
    usize u = algo_unique(arr, n, sizeof(int), fuzz_cmp_int, NULL);

    if (n == 0 && u != 0) __builtin_trap();
    if (n > 0 && (u == 0 || u > n)) __builtin_trap();

    /* Invariant 2: no consecutive equal pairs */
    for (usize i = 1; i < u; i++) {
        if (arr[i] == arr[i-1]) __builtin_trap();
    }

    /* Invariant 5: first element unchanged */
    if (n > 0 && arr[0] != first) __builtin_trap();

    /* Invariant 3: idempotent */
    usize u2 = algo_unique(arr, u, sizeof(int), fuzz_cmp_int, NULL);
    if (u2 != u) __builtin_trap();

    /* Invariant 6: sort-then-unique n equals distinct count */
    algo_sort(arr2, n, sizeof(int), fuzz_cmp_int, NULL, tmp);
    usize su = algo_unique(arr2, n, sizeof(int), fuzz_cmp_int, NULL);
    usize distinct = count_distinct(arr2, n); /* arr2 still sorted up to su */

    /* After unique on sorted array, arr2[0..su-1] is sorted and unique */
    if (su != count_distinct(arr2, su)) __builtin_trap();
    (void)distinct;

    return 0;
}

