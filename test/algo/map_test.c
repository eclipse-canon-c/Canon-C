/*
 * Fuzz algo_map (int → double) and algo_map_inplace (negate) on raw input.
 *
 * Invariants:
 *   algo_map:
 *     1. output[i] == (double)data[i] for all i
 *     2. input unchanged after the call
 *
 *   algo_map_inplace:
 *     3. arr[i] == -original[i] for all i after negate
 *     4. double-negate restores original values
 */

static void fn_fuzz_int_to_double(void* out, const void* in)
{
    *(double*)out = (double)(*(const int*)in);
}

static void fn_fuzz_negate(void* elem)
{
    *(int*)elem = -(*(int*)elem);
}

/* Maximum elements we'll process to keep allocations bounded */
#define MAP_FUZZ_MAX 256

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)map_fuzz_suppress_unused;
    (void)map_slice_suppress_unused;

    /* Need at least sizeof(int) bytes to form one element */
    if (size < sizeof(int)) return 0;

    /* Clamp to max elements */
    usize n = size / sizeof(int);
    if (n > MAP_FUZZ_MAX) n = MAP_FUZZ_MAX;

    /* Copy input bytes into int array */
    int src[MAP_FUZZ_MAX];
    memcpy(src, data, n * sizeof(int));

    /* ── Invariant: algo_map produces correct cast ── */
    double dst[MAP_FUZZ_MAX] = {0};
    algo_map(dst, src, n, sizeof(double), sizeof(int), fn_fuzz_int_to_double);

    for (usize i = 0; i < n; i++) {
        if (dst[i] != (double)src[i]) __builtin_trap();
    }

    /* Input unchanged */
    int src_check[MAP_FUZZ_MAX];
    memcpy(src_check, data, n * sizeof(int));
    for (usize i = 0; i < n; i++) {
        if (src[i] != src_check[i]) __builtin_trap();
    }

    /* ── Invariant: algo_map_inplace negate is self-inverse ── */
    int arr[MAP_FUZZ_MAX];
    memcpy(arr, src, n * sizeof(int));

    algo_map_inplace(arr, n, sizeof(int), fn_fuzz_negate);

    /* Verify negate: arr[i] == -src[i] */
    for (usize i = 0; i < n; i++) {
        if (arr[i] != -src[i]) __builtin_trap();
    }

    /* Double-negate restores original */
    algo_map_inplace(arr, n, sizeof(int), fn_fuzz_negate);
    for (usize i = 0; i < n; i++) {
        if (arr[i] != src[i]) __builtin_trap();
    }

    return 0;
}

