/**
 * @file map_test.c
 * @brief Tests for algo/map — element-wise transformation over sequences
 *
 * Four API surfaces tested:
 *   algo_map              — generic cross-type transform (void* interface)
 *   algo_map_inplace      — generic in-place transform (void* interface)
 *   ALGO_MAP_TYPED        — typed macro, compile-time sizeof, cross-type
 *   ALGO_MAP_INPLACE_TYPED — typed macro, compile-time sizeof, in-place
 *   algo_map_slice_##in##_##out — typed slice, cross-type
 *   algo_map_inplace_slice_##type — typed slice, in-place
 *
 * Two instantiations:
 *   int × double — primary cross-type case
 *   int × int    — same-type / in-place case; also exercises Point struct
 *
 * Covers:
 *   - Empty sequence (len=0)          — no fn call, output unchanged
 *   - Single element                  — transform applied once
 *   - Multiple elements               — all transformed correctly
 *   - Cross-type transform (int→double) — type conversion verified
 *   - Same-type in-place transform    — original overwritten
 *   - Typed macro (ALGO_MAP_TYPED)    — compile-time sizeof, same semantics
 *   - Typed macro (ALGO_MAP_INPLACE_TYPED) — compile-time sizeof
 *   - Typed slice cross-type          — algo_map_slice_int_double
 *   - Typed slice in-place            — algo_map_inplace_slice_int
 *   - Mismatched slice lengths        — min(sv_out.len, sv_in.len) processed
 *   - fn call count                   — always exactly n calls
 *   - Input unchanged after algo_map  — verified explicitly
 *   - Point struct                    — algo_map_inplace_slice_Point
 *
 * Fuzz entry point (CANON_FUZZING):
 *   - algo_map: int[] → double[] — reference: cast each input
 *   - algo_map_inplace: negate each element — reference: independent copy
 *   - Invariants:
 *       • output[i] == (double)input[i] for all i
 *       • after inplace: arr[i] == -original[i] for all i
 *       • fn called exactly n times (verified via call counter)
 */

