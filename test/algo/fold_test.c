/**
 * @file fold_test.c
 * @brief Tests for algo/fold — functional reduction of sequences to single values
 *
 * Five API surfaces tested:
 *   ALGO_FOLD              — infallible macro, typed array
 *   ALGO_FOLD_RESULT       — fallible macro, typed array (GNU mode)
 *   ALGO_FOLD_VEC          — infallible macro over .items/.len container
 *   algo_fold_slice_##type — typed slice, infallible
 *   algo_fold_result_slice_##type — typed slice, fallible
 *
 * Two instantiations:
 *   int   — primary; all five surfaces exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - Empty sequence          — infallible: acc unchanged; fallible: Ok(true)
 *   - Single element          — matching and error-inducing
 *   - All elements succeed    — full scan, correct accumulation
 *   - First element fails     — fallible stops immediately, 1 fn call
 *   - Middle element fails    — partial accumulation up to error point
 *   - Last element fails      — n-1 elements accumulated
 *   - Accumulator type != element type — fold int[] into double sum
 *   - Context parameter       — fn uses ctx to parameterize behavior
 *   - fn call count           — infallible: always n; fallible: stops at error
 *   - ALGO_FOLD_VEC           — .items/.len container wrapper
 *   - ALGO_FOLD_RESULT C99    — do-while variant under CANON_NO_GNU_EXTENSIONS
 *   - result_bool_Error_is_ok / is_err semantics
 *   - Acc unchanged on empty  — acc initialized before call, unchanged after
 *
 * Fuzz entry point (CANON_FUZZING):
 *   - Infallible sum fold vs reference sum — must match exactly
 *   - Fallible fold stops at first zero byte — verifies partial accumulation
 *   - Invariants:
 *       • infallible sum == reference byte sum (mod overflow)
 *       • fallible result is Err iff any byte is zero
 *       • fallible acc == sum of bytes before first zero
 */

