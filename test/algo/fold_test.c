int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)fold_fuzz_suppress_unused;
    (void)fold_slice_suppress_unused;
    (void)fold_result_suppress_unused;
    (void)fold_u8_suppress;

    if (size == 0) return 0;

    /* ── Infallible fold ── */
    u32 fold_sum = 0;
    ALGO_FOLD(&fold_sum, data, size, u8, fn_sum_u32, NULL);

    /* Reference sum */
    u32 ref_sum = 0;
    for (usize i = 0; i < size; i++) ref_sum += (u32)data[i];

    if (fold_sum != ref_sum) __builtin_trap();

    /* ── Fallible fold: sum until first zero ── */
    u32 partial_sum = 0;
    result_bool_Error r;
    FOLD_RESULT(&partial_sum, data, size, u8, fn_sum_nonzero_u32, NULL, r);

    /* Find reference: index of first zero */
    usize first_zero = size; /* sentinel: no zero found */
    for (usize i = 0; i < size; i++) {
        if (data[i] == 0) { first_zero = i; break; }
    }

    bool has_zero = (first_zero < size);

    /* Invariant: Err iff has zero */
    if (result_bool_Error_is_err(r) != has_zero) __builtin_trap();

    /* Invariant: partial_sum == sum of bytes before first zero (or all bytes) */
    u32 expected_partial = 0;
    usize limit = has_zero ? first_zero : size;
    for (usize i = 0; i < limit; i++) expected_partial += (u32)data[i];
    if (partial_sum != expected_partial) __builtin_trap();

    return 0;
}

