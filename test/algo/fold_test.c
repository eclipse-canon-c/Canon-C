/*
 * Fuzz both infallible and fallible fold on a raw byte array.
 *
 * Infallible fold: sum all bytes into a u32 accumulator.
 *   Reference: independent loop sum.
 *   Invariant: fold sum == reference sum (both computed with same overflow).
 *
 * Fallible fold: sum bytes, fail on zero.
 *   Reference: index of first zero byte (if any).
 *   Invariants:
 *     • result is Err iff input contains a zero byte
 *     • if Err: acc == sum of bytes before first zero
 *     • if Ok:  acc == sum of all bytes
 */

static void fn_sum_u32(void* acc, const u8* elem, void* ctx)
{
    (void)ctx;
    *(u32*)acc += (u32)*elem;
}

static result_bool_Error fn_sum_nonzero_u32(void* acc, const u8* elem, void* ctx)
{
    (void)ctx;
    if (*elem == 0) return result_bool_Error_err(ERR_INVALID_ARG);
    *(u32*)acc += (u32)*elem;
    return result_bool_Error_ok(true);
}

