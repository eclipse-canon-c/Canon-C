/*
 * Fuzz algo_reverse and algo_is_palindrome on raw byte input (treated as int[]).
 *
 * Invariants:
 *   1. Double-reverse restores original: reverse(reverse(arr)) == arr
 *   2. Palindrome status is invariant under reversal:
 *      is_palindrome(arr) == is_palindrome(reverse(arr))
 */

static int fuzz_cmp_int(const void* a, const void* b, void* ctx)
{
    (void)ctx;
    return *(const int*)a - *(const int*)b;
}

#define REVERSE_FUZZ_MAX 256

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)reverse_fuzz_suppress_unused;
    (void)reverse_slice_suppress_unused;

    if (size < sizeof(int)) return 0;

    usize n = size / sizeof(int);
    if (n > REVERSE_FUZZ_MAX) n = REVERSE_FUZZ_MAX;

    int original[REVERSE_FUZZ_MAX];
    int arr[REVERSE_FUZZ_MAX];
    memcpy(original, data, n * sizeof(int));
    memcpy(arr,      data, n * sizeof(int));

    /* Invariant 1: double-reverse is identity */
    algo_reverse(arr, n, sizeof(int));
    algo_reverse(arr, n, sizeof(int));

    for (usize i = 0; i < n; i++) {
        if (arr[i] != original[i]) __builtin_trap();
    }

    /* Invariant 2: palindrome status invariant under reversal */
    bool pal_before = algo_is_palindrome(original, n, sizeof(int),
                                         fuzz_cmp_int, NULL);

    /* Reverse once */
    algo_reverse(arr, n, sizeof(int));

    bool pal_after = algo_is_palindrome(arr, n, sizeof(int),
                                        fuzz_cmp_int, NULL);

    if (pal_before != pal_after) __builtin_trap();

    return 0;
}

