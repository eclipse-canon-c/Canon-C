/**
 * @file reverse_test.c 
 * @brief Tests for algo/reverse — in-place reversal and palindrome checking
 *
 * Two algorithms, three API levels each:
 *   algo_reverse           — generic in-place reversal (void* interface)
 *   ALGO_REVERSE_TYPED     — typed macro, compile-time sizeof
 *   algo_reverse_slice_##type — typed slice
 *
 *   algo_is_palindrome         — generic palindrome check (void* interface)
 *   ALGO_IS_PALINDROME_TYPED   — typed macro, compile-time sizeof
 *   algo_is_palindrome_slice_##type — typed slice
 *
 * Two instantiations:
 *   int   — primary; all three levels exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - Empty (len=0)            — reverse: no-op; palindrome: true
 *   - Single element (len=1)   — reverse: no-op; palindrome: true
 *   - Even length              — even pair count, no center element
 *   - Odd length               — center element stays in place
 *   - Already reversed         — reverse twice = identity
 *   - True palindrome          — palindrome returns true
 *   - Non-palindrome           — palindrome returns false
 *   - First pair mismatch      — palindrome short-circuits at pair 0
 *   - Last pair mismatch       — palindrome short-circuits near center
 *   - Typed macro              — ALGO_REVERSE_TYPED / ALGO_IS_PALINDROME_TYPED
 *   - Typed slice              — algo_reverse_slice_int / algo_is_palindrome_slice_int
 *   - Point struct             — algo_reverse_slice_Point / algo_is_palindrome_slice_Point
 *   - Context parameter        — cmp uses ctx for threshold
 *   - Dual relationship        — reverse then palindrome check on known results
 *
 * Fuzz entry point (CANON_FUZZING):
 *   - algo_reverse is self-inverse: reverse twice = original
 *   - algo_is_palindrome after reverse: must equal palindrome check on original
 *   - Invariants:
 *       • double-reverse restores original byte-for-byte
 *       • is_palindrome(arr) == is_palindrome(reverse(arr))
 *         (palindrome status is invariant under reversal)
 */

