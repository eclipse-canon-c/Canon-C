/*
 * Fuzz algo_find on a raw byte array.
 *
 * Predicate: first byte with value > 127.
 *
 * Invariants verified:
 *   1. found ↔ any byte > 127 exists
 *   2. if found: out_index == reference index
 *   3. if found: element at out_index satisfies pred
 *   4. if found: no element before out_index satisfies pred
 *   5. if not found: no element satisfies pred
 *   6. if not found: outputs unchanged (sentinel values preserved)
 */

static bool pred_high_byte(const void* elem, void* ctx)
{
    (void)ctx;
    return *(const u8*)elem > 127;
}

int LLVMFuzzerTestOneInput(const u8* data, usize size)
{
    (void)find_fuzz_suppress_unused;
    (void)find_slice_suppress_unused;
    (void)find_option_suppress_unused;

    /* Sentinel values — must not be overwritten on no-match */
    usize out_idx      = CANON_USIZE_MAX - 1;
    const void* out_ep = NULL;

    bool found = algo_find(data, size, sizeof(u8), pred_high_byte, NULL,
                           &out_idx, &out_ep);

    /* Build reference: index of first byte > 127 */
    usize ref_idx = CANON_USIZE_MAX;
    for (usize i = 0; i < size; i++) {
        if (data[i] > 127) { ref_idx = i; break; }
    }

    bool ref_found = (ref_idx != CANON_USIZE_MAX);

    /* Invariant 1: found ↔ reference found */
    if (found != ref_found) __builtin_trap();

    if (found) {
        /* Invariant 2: index matches reference */
        if (out_idx != ref_idx) __builtin_trap();

        /* Invariant 3: element at index satisfies pred */
        if (data[out_idx] <= 127) __builtin_trap();

        /* Invariant 4: no earlier element satisfies pred */
        for (usize i = 0; i < out_idx; i++) {
            if (data[i] > 127) __builtin_trap();
        }

        /* Invariant: out_ep points to correct element */
        if (out_ep != (const void*)(data + out_idx)) __builtin_trap();

    } else {
        /* Invariant 5: no element satisfies pred */
        for (usize i = 0; i < size; i++) {
            if (data[i] > 127) __builtin_trap();
        }

        /* Invariant 6: sentinels unchanged */
        if (out_idx != CANON_USIZE_MAX - 1) __builtin_trap();
        if (out_ep  != NULL)                 __builtin_trap();
    }

    return 0;
}

