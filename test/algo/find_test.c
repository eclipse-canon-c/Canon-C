/* ── test_null_outputs ───────────────────────────────────────────────────── */
static void test_null_outputs(void)
{
    int arr[] = {1, -2, 3};

    /* Both outputs NULL — still returns true/false correctly */
    bool found = algo_find(arr, 3, sizeof(int), pred_negative, NULL,
                           NULL, NULL);
    EXPECT(found);

    /* Index NULL only */
    const int* ep = NULL;
    found = algo_find(arr, 3, sizeof(int), pred_negative, NULL,
                      NULL, (const void**)&ep);
    EXPECT(found && ep != NULL && *ep == -2);

    /* Elem NULL only */
    usize idx = 99;
    found = algo_find(arr, 3, sizeof(int), pred_negative, NULL,
                      &idx, NULL);
    EXPECT(found && idx == 1);

    /* Not found, both NULL — safe */
    found = algo_find(arr, 3, sizeof(int), pred_always_false, NULL,
                      NULL, NULL);
    EXPECT(!found);
}

/* ── test_context_parameter ──────────────────────────────────────────────── */
static void test_context_parameter(void)
{
    int arr[] = {1, 5, 3, 8, 2};

    /* Find first element > 6 — should be 8 at index 3 */
    int threshold = 6;
    usize idx = 99;
    bool found = algo_find(arr, 5, sizeof(int), pred_gt_threshold, &threshold,
                           &idx, NULL);
    EXPECT(found && idx == 3);

    /* Find specific value */
    int target = 3;
    idx = 99;
    found = algo_find(arr, 5, sizeof(int), pred_eq_value, &target, &idx, NULL);
    EXPECT(found && idx == 2);

    /* Value not present */
    target = 99;
    found = algo_find(arr, 5, sizeof(int), pred_eq_value, &target, &idx, NULL);
    EXPECT(!found);
}

/* ── test_typed_macro ────────────────────────────────────────────────────── */
static void test_typed_macro(void)
{
    int arr[] = {10, 20, -30, 40};
    usize idx = 99;
    const int* ep = NULL;

    bool found = ALGO_FIND_TYPED(arr, 4, int, pred_negative, NULL, &idx, &ep);
    EXPECT(found && idx == 2 && ep != NULL && *ep == -30);

    /* Not found */
    idx = 99; ep = NULL;
    found = ALGO_FIND_TYPED(arr, 4, int, pred_always_false, NULL, &idx, &ep);
    EXPECT(!found && idx == 99 && ep == NULL);

    /* NULL outputs via typed macro */
    found = ALGO_FIND_TYPED(arr, 4, int, pred_negative, NULL, NULL, NULL);
    EXPECT(found);

    /* Empty */
    found = ALGO_FIND_TYPED(arr, 0, int, pred_negative, NULL, NULL, NULL);
    EXPECT(!found);
}

/* ── test_option_variant ─────────────────────────────────────────────────── */
static void test_option_variant(void)
{
    int arr[] = {5, 3, -2, -7, 1};
    slice_int sv = slice_int_from(arr, 5);

    /* Found: Some(-2) */
    option_int opt = algo_find_slice_int(sv, pred_negative, NULL);
    EXPECT(option_int_is_some(opt));
    EXPECT(option_int_unwrap(opt) == -2);

    /* Not found: None */
    int pos_arr[] = {1, 2, 3};
    slice_int sv2 = slice_int_from(pos_arr, 3);
    opt = algo_find_slice_int(sv2, pred_negative, NULL);
    EXPECT(!option_int_is_some(opt));

    /* Empty: None */
    slice_int empty = slice_int_empty();
    opt = algo_find_slice_int(empty, pred_negative, NULL);
    EXPECT(!option_int_is_some(opt));

    /* Single-element: found */
    int single[] = {-99};
    slice_int sv3 = slice_int_from(single, 1);
    opt = algo_find_slice_int(sv3, pred_negative, NULL);
    EXPECT(option_int_is_some(opt) && option_int_unwrap(opt) == -99);
}

/* ── test_index_variant ──────────────────────────────────────────────────── */
static void test_index_variant(void)
{
    int arr[] = {1, 2, -3, 4, -5};
    slice_int sv = slice_int_from(arr, 5);

    /* First negative at index 2 */
    usize i = algo_find_index_slice_int(sv, pred_negative, NULL);
    EXPECT(i == 2);

    /* Not found → CANON_USIZE_MAX */
    int pos[] = {1, 2, 3};
    slice_int sv2 = slice_int_from(pos, 3);
    i = algo_find_index_slice_int(sv2, pred_negative, NULL);
    EXPECT(i == CANON_USIZE_MAX);

    /* Empty → CANON_USIZE_MAX */
    slice_int empty = slice_int_empty();
    i = algo_find_index_slice_int(empty, pred_negative, NULL);
    EXPECT(i == CANON_USIZE_MAX);

    /* CANON_USIZE_MAX is never a valid index */
    EXPECT(CANON_USIZE_MAX > (usize)5); /* sanity: larger than any array used here */
}

/* ── test_point_struct ───────────────────────────────────────────────────── */
static void test_point_struct(void)
{
    Point arr[] = {{1, 2}, {3, 3}, {5, 4}, {7, 7}};
    slice_Point sv = slice_Point_from(arr, 4);

    /* Find first point where x == y: {3,3} at index 1 */
    option_Point opt = algo_find_slice_Point(sv, pred_point_eq, NULL);
    EXPECT(option_Point_is_some(opt));
    Point val = option_Point_unwrap(opt);
    EXPECT(val.x == 3 && val.y == 3);

    usize i = algo_find_index_slice_Point(sv, pred_point_eq, NULL);
    EXPECT(i == 1);

    /* None match */
    Point no_eq[] = {{1, 2}, {3, 4}};
    slice_Point sv2 = slice_Point_from(no_eq, 2);
    opt = algo_find_slice_Point(sv2, pred_point_eq, NULL);
    EXPECT(!option_Point_is_some(opt));

    i = algo_find_index_slice_Point(sv2, pred_point_eq, NULL);
    EXPECT(i == CANON_USIZE_MAX);

    /* Empty */
    slice_Point empty = slice_Point_empty();
    opt = algo_find_slice_Point(empty, pred_point_eq, NULL);
    EXPECT(!option_Point_is_some(opt));
}

/* ── test_short_circuit ──────────────────────────────────────────────────── */
static void test_short_circuit(void)
{
    /* Match at index 2 — pred called exactly 3 times */
    int arr[] = {1, 2, -3, -4, -5};
    g_call_count = 0;
    usize idx = 99;
    bool found = algo_find(arr, 5, sizeof(int), pred_negative_counting,
                           NULL, &idx, NULL);
    EXPECT(found && idx == 2);
    EXPECT(g_call_count == 3); /* checked 0, 1, 2 — stopped at 2 */

    /* No match — pred called for all 5 elements */
    int pos[] = {1, 2, 3, 4, 5};
    g_call_count = 0;
    found = algo_find(pos, 5, sizeof(int), pred_negative_counting,
                      NULL, NULL, NULL);
    EXPECT(!found);
    EXPECT(g_call_count == 5);

    /* Match at index 0 — pred called exactly once */
    int first_neg[] = {-9, 1, 2, 3, 4};
    g_call_count = 0;
    found = algo_find(first_neg, 5, sizeof(int), pred_negative_counting,
                      NULL, &idx, NULL);
    EXPECT(found && idx == 0);
    EXPECT(g_call_count == 1);
}

