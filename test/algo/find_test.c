/**
 * @file find_test.c
 * @brief Tests for algo/find — locate first element matching a predicate
 *
 * Three API levels tested:
 *   Level 1 — Generic (algo_find): void* interface, bool return, optional outputs
 *   Level 2 — Typed macro (ALGO_FIND_TYPED): compile-time sizeof, typed out pointer
 *   Level 3 — Typed slice:
 *       algo_find_slice_int        → option_int  (Some on match, None otherwise)
 *       algo_find_index_slice_int  → usize       (CANON_USIZE_MAX if not found)
 *
 * Two instantiations:
 *   int   — primary; all three levels exercised
 *   Point — struct coverage; slice level only
 *
 * Covers:
 *   - algo_find()                   — basic search
 *   - ALGO_FIND_TYPED()             — same semantics, automatic sizeof
 *   - algo_find_slice_int()         — returns option_int
 *   - algo_find_index_slice_int()   — returns usize index
 *   - algo_find_slice_Point()
 *   - algo_find_index_slice_Point()
 *   - Empty sequence (len=0)        — returns false / None / CANON_USIZE_MAX
 *   - Not found                     — returns false, outputs NOT written
 *   - Match at first element        — index 0
 *   - Match at last element         — index n-1
 *   - Match in middle               — correct index returned
 *   - First match wins              — multiple matches, only first returned
 *   - NULL output parameters        — both out_index and out_elem may be NULL
 *   - Output not written on miss    — output vars unchanged when pred never matches
 *   - Context parameter             — pred uses ctx to parameterize
 *   - Short-circuit                 — pred not called after first match
 *   - option_int_is_some / unwrap   — option semantics verified
 *   - CANON_USIZE_MAX sentinel      — verified on no-match
 *
 * Fuzz entry point (CANON_FUZZING):
 *   - Finds first byte > 127 in a raw byte array
 *   - Reference model: independent linear scan
 *   - Invariants:
 *       • found ↔ reference found
 *       • if found: index matches reference index
 *       • if found: element satisfies pred
 *       • if found: no element before index satisfies pred
 *       • if not found: no element in array satisfies pred
 */

